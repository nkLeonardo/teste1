Fahrenheit= int (input("Coloque a temperatura em Fahrenheit: "))
Celcius= float ((Fahrenheit - 32) * 5/9)
Celciusf= float (f"{Celcius: .2f}")

print("A temperatura é: C°", Celciusf)
